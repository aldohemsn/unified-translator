def check_length(text, limit=25):
    lines = text.strip().split('\n')
    over_limit = []
    
    print(f"{'Line #':<8} {'Length':<8} {'Content'}")
    print("-" * 60)
    
    for i, line in enumerate(lines, 1):
        clean_line = line.strip()
        if not clean_line or clean_line.startswith("Target") or clean_line.startswith("画面字：") or clean_line.startswith("02:"):
            continue
            
        # Count characters (including spaces/punctuation as they take screen space)
        length = len(clean_line)
        
        if length > limit:
            over_limit.append((i, length, clean_line))
            print(f"{i:<8} {length:<8} {clean_line}")

    print(f"\nFound {len(over_limit)} lines exceeding {limit} characters.")

check_length(text)
